package jasd.number.equation;

public final class VariableKey
{
    String s;
    double d;
    
    /**
     * Creates a value for a variable with the name.
     * @param s Name of the variable.
     * @param d Value of the variable.
     */
    public VariableKey(String s, double d)
    {
        this.s = s;
        this.d = d;
    }
}
